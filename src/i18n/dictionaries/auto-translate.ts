export function translateText(value: string, locale: string) {
  if (locale !== "zh-CN") {
    return value;
  }

  const trimmed = value.trim();
  if (!trimmed) {
    return value;
  }

  const exact = zhExactText[trimmed];
  if (exact) {
    return preserveWhitespace(value, exact);
  }

  let translated = trimmed;
  for (const [source, target] of zhReplacements) {
    translated = translated.replaceAll(source, target);
  }

  return translated === trimmed ? value : preserveWhitespace(value, translated);
}

function preserveWhitespace(original: string, translated: string) {
  const prefix = original.match(/^\s*/)?.[0] ?? "";
  const suffix = original.match(/\s*$/)?.[0] ?? "";
  return `${prefix}${translated}${suffix}`;
}
